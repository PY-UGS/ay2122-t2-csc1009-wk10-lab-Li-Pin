# Calculate average for the 2 numbers
def average(first, sec):
    return (first + sec) / 2


# Take in user input for 2 numbers
firstNum = int(input("Please enter the first number: "))
secNum = int(input("Please enter the second number: "))

# Print average of the 2 numbers
print("Average of the 2 numbers: ", average(firstNum, secNum))
